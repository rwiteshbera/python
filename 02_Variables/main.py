# Python is completely object oriented
# Dynamically Typed
# You don't need to declare variable types before using them

age = 21
print(age)

greetings = "Hello"
print(greetings)

a, b = 10, 20
print(a)
print(b)

one = 1
two = 2
hello = "Hello" 
print(one + two + hello) # Mixing operators between number and string is not supported